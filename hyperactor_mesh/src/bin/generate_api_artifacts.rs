//! Generates API artifact snapshots for the mesh admin API.
//!
//! Produces:
//! - JSON Schema snapshots (raw schemars output without `$id`;
//!   serve endpoints add `$id` at runtime per SC-4)
//! - OpenAPI 3.1 spec (embeds the JSON Schemas)
//!
//! Snapshot tests in `introspect::tests` compare against these
//! files; any type change that alters a schema will fail CI until
//! the artifacts are regenerated.
//!
//! ## Usage
//!
//! Buck:
//! ```sh
//! buck run fbcode//monarch/hyperactor_mesh:generate_api_artifacts \
//!   @fbcode//mode/dev-nosan -- \
//!   fbcode/monarch/hyperactor_mesh/src/testdata
//! ```
//!
//! Cargo:
//! ```sh
//! cargo run -p hyperactor_mesh --bin generate_api_artifacts -- \
//!   hyperactor_mesh/src/testdata
//! ```
//!
//! Writes:
//! - `node_payload_schema.json` — `NodePayload` schema (SC-2)
//! - `error_schema.json` — `ApiErrorEnvelope` schema (SC-2)
//! - `openapi.json` — OpenAPI 3.1 spec

use hyperactor_mesh::introspect::NodePayload;
use hyperactor_mesh::mesh_admin::ApiErrorEnvelope;

/// Marker injected into every generated JSON file so Phabricator
/// collapses them in diff review by default.
///
/// The literal is constructed via `concat!` so the source file itself
/// does not contain the contiguous sigil that Phabricator pattern-matches.
const GENERATED_COMMENT: &str = concat!(
    "@",
    "generated by generate_api_artifacts — do not edit. ",
    "Regenerate: buck run fbcode//monarch/hyperactor_mesh:generate_api_artifacts ",
    "@fbcode//mode/dev-nosan -- fbcode/monarch/hyperactor_mesh/src/testdata",
);

fn main() {
    let out_dir = std::env::args()
        .nth(1)
        .unwrap_or_else(|| "src/testdata".into());

    std::fs::create_dir_all(&out_dir).expect("failed to create output directory");

    write_schema::<NodePayload>(&out_dir, "node_payload_schema.json");
    write_schema::<ApiErrorEnvelope>(&out_dir, "error_schema.json");
    write_openapi(&out_dir);
}

/// Insert a `$comment` field with the `@generated` marker into a
/// JSON object value.
fn insert_generated_comment(value: &mut serde_json::Value) {
    if let Some(obj) = value.as_object_mut() {
        obj.insert(
            "$comment".into(),
            serde_json::Value::String(GENERATED_COMMENT.into()),
        );
    }
}

fn write_schema<T: schemars::JsonSchema>(out_dir: &str, filename: &str) {
    let schema = schemars::schema_for!(T);
    let mut value = serde_json::to_value(schema).expect("schema must be serializable");
    insert_generated_comment(&mut value);
    let json = serde_json::to_string_pretty(&value).expect("schema must be serializable");
    let path = format!("{out_dir}/{filename}");
    std::fs::write(&path, format!("{json}\n")).expect("failed to write schema file");
    eprintln!("wrote {path}");
}

fn write_openapi(out_dir: &str) {
    let mut spec = hyperactor_mesh::mesh_admin::build_openapi_spec();
    insert_generated_comment(&mut spec);
    let json = serde_json::to_string_pretty(&spec).expect("OpenAPI spec must be serializable");
    let path = format!("{out_dir}/openapi.json");
    std::fs::write(&path, format!("{json}\n")).expect("failed to write OpenAPI spec");
    eprintln!("wrote {path}");
}
